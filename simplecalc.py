# Building a simple calculator
# use the input func to ask a user for an input

first_num = float(input('Please Enter a number: '))
second_num = float(input('Please Enter another number: '))

# addition
print(first_num + second_num)
# subtraction
print(first_num - second_num)
# multiplication
print(first_num * second_num)
# division
print(first_num / second_num)
# floor division
print(first_num // second_num)
# modulus division
print(first_num % second_num)
